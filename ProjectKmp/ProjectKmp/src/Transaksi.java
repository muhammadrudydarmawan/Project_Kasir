    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        // TODO add your handling code here:
        try {

            Connection con = ConnectionHelper.getConnection();
            int row = tfTabel.getSelectedRow();
            String value = (tfTabel.getModel().getValueAt(row, 0).toString());
            String sql = "UPDATE transaksi SET no_stand=?,nama_barang=?,harga_satuan=?,jumlah_beli=? WHERE no_stand=" + value;
            if (noStandTxt.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "No Stand tidak boleh kosong");
            } else if (barangTxt.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Barang tidak boleh kosong");
            } else if (hargaBrg.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Harga tidak boleh kosong");
            } else if (jumlahBrg.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Jumlah tidak boleh kosong");
            } else {
                pst = con.prepareStatement(sql);
                pst.setString(1, noStandTxt.getText());
                pst.setString(2, barangTxt.getText());
                pst.setString(3, hargaBrg.getText());
                pst.setString(4, jumlahBrg.getText());

                pst.executeUpdate();

                DefaultTableModel model = (DefaultTableModel) tfTabel.getModel();
                model.setRowCount(0);
                loadTF();
                noStandTxt.setText("");
                barangTxt.setText("");
                hargaBrg.setText("");
                jumlahBrg.setText("");
                totalSemua.setText("");
                JOptionPane.showMessageDialog(null, "Update Selesai");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_editBtnActionPerformed

