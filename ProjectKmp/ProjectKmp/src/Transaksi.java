    public void initTableTF() {

        String[] pelangganColumns = new String[]{"No.Stand", "Nama Barang", "Harga Satuan", "Jumblah Beli"};

        int[] columnWidth = { //            135, 135, 135, 135 //lebar tiap kolom
        };

        tableModel = new DefaultTableModel(pelangganColumns, 0);
        tfTabel.setModel(tableModel);
        tfTabel.setRowHeight(20);

        int i = 0;
        for (int width : columnWidth) {
            TableColumn column = tfTabel.getColumnModel().getColumn(i++);
            column.setMaxWidth(width);
            column.setPreferredWidth(width);
        }
    }

    public static void loadTF() {
        listTransaksiModel = new ArrayList<>();
        listTransaksiModel = TransaksiManager.shoTransaksiModel();
        tableModel.setRowCount(0);
        listTransaksiModel.forEach(TransaksiModel -> {
            tableModel.addRow(new Object[]{
                TransaksiModel.getNo_stand(),
                TransaksiModel.getNama_barang(),
                TransaksiModel.getHarga_satuan(),
                TransaksiModel.getJumlah_beli()
            });
        });
    }

